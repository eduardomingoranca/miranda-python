# O que eh JSON - JavaScript Object Notation
# JSON - JavaScript Object Notation (extensao .json)
# Eh uma estrutura de dados que permite a serializacao
# de objetos em texto simples para facilitar a transmissao de
# dados atraves da rede, APIs web ou outros meios de comunicacao.
# O JSON suporta os seguintes tipos de dados:
# Numeros: podem ser inteiros ou com ponto flutuante, como 42 ou 3.14
# Strings: sao cadeias de caracteres, como "Ola, mundo!" ou "12345"
#   As strings devem ser envolvidas por aspas duplas
# Booleanos: sao os valores verdadeiro (true) ou falso (false)
# Arrays: sao listas ordenadas de valores, como [1, 2, 3] ou
#   ["Oi", "Ola", "Bom dia"]
# Objetos: sao conjuntos de pares chave/valor -> {"nome": "John", "idade": 30}
# null: eh um valor especial que representa ausencia de valor
#
# Ao converter de Python para JSON:
# Python        JSON
# dict          object
# list, tuple   array
# str           string
# int, float    number
# True          true
# False         false
# None          null