"""
Python = Linguagem de programacao
Tipo de tipagem = Dinamica / Forte
str -> string -> texto
Strings sao textos que estao dentro de aspas
"""
print(1234)

# Aspas simples
print('Louis Octavius')
print(1, 'Louis "Octavius"')

# Aspas duplas
print("Louis Octavius")
print(2, "Louis 'Octavius'")


# Escape
print('Louis \'Octavius\'')

# r
print(r'Louis \'Octavius\'')
